class Relatorio {
    private int id;
    private Date dataGeracao;
    private float consumoTotal;

    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public Date getDataGeracao() { return dataGeracao; }
    public void setDataGeracao(Date dataGeracao) { this.dataGeracao = dataGeracao; }

    public float getConsumoTotal() { return consumoTotal; }
    public void setConsumoTotal(float consumoTotal) { this.consumoTotal = consumoTotal; }
}
